// Different naming conventions used in JavaScript:

// 1. camelCase (most common for variables & functions)
var firstName = "Shyama";
var lastName = "PM";
var getUserAge = function () { return 25; };

// 2. PascalCase (used for classes & constructors) // used for OOPS concept
let UserProfile = "PascalCase"; // Not a class, but named like one
let ShoppingCart = "class name style"; // Not a class, but named like one


// 3. snake_case (often used in databases, Python-style)
var first_name = "Shyama";
var last_name = "PM";
var user_email = "shyama@email.com";

// 4. SCREAMING_SNAKE_CASE (used for constants)
var MAX_VALUE = 100;
var API_KEY = "abc123";
var DEFAULT_TIMEOUT = 5000;

// 5. kebab-case (NOT valid in JS — but valid in CSS / file names)
// var first-name = "Shyama";  // ❌ SyntaxError — hyphens not allowed

// 6. Hungarian Notation (prefix with type — less common now)
var strName = "Shyama"; // str = string
var intCount = 10; // int = integer
var arrItems = [1, 2, 3]; // arr = array
var fnCallback = function () {}; // fn = function
